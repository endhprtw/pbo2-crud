    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        String status;
        
        
        status = (String) cbStatus.getSelectedItem();
        
        karyawan.setNip(txtNIP.getText());
        karyawan.setNama(txtNama.getText());
        karyawan.setAlamat(txtAlamat.getText());
        karyawan.setNoHP(txtNo_HP.getText());
        karyawan.setStatus(status);
        
        try {
            karyawanctrl.create(karyawan);
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
            tablekaryawan();
            //tableanggota();
            
            txtNIP.setText("");
            txtNama.setText("");
            txtAlamat.setText("");
            txtNo_HP.setText("");
            cbStatus.setSelectedIndex(0);
            
            
        } catch (Exception ex) {
            Logger.getLogger(formkaryawan.class.getName()).log(Level.SERVE, null, ex);
        }
    }//GEN-LAST:event_btSimpanActionPerformed

