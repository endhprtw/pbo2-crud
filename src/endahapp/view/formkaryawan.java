    public void tablekaryawan(){
        
        Object header[]={"NIP","Nama","Alamat","No HP","Status"}; 
        DefaultTableModel data=new DefaultTableModel(null, header);
        
        List<Karyawan> listkaryawan=new java.util.ArrayList<>();
        listkaryawan=karyawanctrl.findKaryawanEntities();
        
        if (listkaryawan.size()==0){
            tKaryawan.setModel(data);
        }else{
            for(int i=0;i<listkaryawan.size();i++){
                karyawan=listkaryawan.get(i);
                String row [] ={
                    karyawan.getNip(),
                    karyawan.getNama(),
                    karyawan.getAlamat(),
                    karyawan.getNoHP(),
                    karyawan.getStatus()
                };
                data.addRow(row);
                tKaryawan.setModel(data);
            }
        }
    }

